    private void remover_bttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_remover_bttActionPerformed
        Editar_pedido pedido = new Editar_pedido();
        DAO_pedido crud = new DAO_pedido();
        
        //checa se existe um pedido com aquele cpf
        boolean rs = crud.checkPedido(cpf);
        if (rs){
            //puxa as variaveis do pedido ja feito para abrir a interface de edicao do pedido
            pedido.setCPF(cpf);
            pedido.pedidoAnterior();
            pedido.setVisible(true);
            this.setVisible(false);   
        }
        else{
            JOptionPane.showMessageDialog(null, "Você não possuí nenhum um pedido em andamento.", "Remover produtos", JOptionPane.ERROR_MESSAGE);
        } 
    }//GEN-LAST:event_remover_bttActionPerformed

