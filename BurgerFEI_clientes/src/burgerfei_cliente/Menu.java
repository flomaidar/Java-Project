    private void cancelar_bttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelar_bttActionPerformed
        DAO_pedido crud = new DAO_pedido();
        
        //checa se existe pedido naquele cpf
        boolean rs = crud.checkPedido(cpf);
        if (rs){
            //vai para interface de cancelar pedido
            new Cancelar_pedido().setVisible(true);
            this.setVisible(false);
        }
        else{
            JOptionPane.showMessageDialog(null, "Você não possuí nenhum um pedido em andamento.", "Remover produtos", JOptionPane.ERROR_MESSAGE);
        } 
        
    }//GEN-LAST:event_cancelar_bttActionPerformed

    private void extrato_bttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_extrato_bttActionPerformed
        ArrayList <String> lista = new ArrayList<>();
        DAO_pedido crud = new DAO_pedido();
        
        //checa se existe pedido naquele cpf
        boolean rs2 = crud.checkPedido(cpf);
        if (rs2){
            try {
                //pega todos os dados da tabela pelo cpf
                ResultSet rs = crud.buscarPorCPF(cpf);
                while (rs.next()) {
                    if(rs.getInt("SALADA")!=0)
                        lista.add("X-SALADA                          "+ rs.getInt("SALADA")+"x R$10.00      (R$"+ String.format("%.2f",(rs.getInt("SALADA")*10.0))+")\n");
                    if(rs.getInt("BURGER")!=0)
                        lista.add("X-BURGER                           "+ rs.getInt("BURGER")+"x R$10.00      (R$"+ String.format("%.2f",(rs.getInt("BURGER")*10.0))+")\n");
                    if(rs.getInt("CACHORRO")!=0)
                        lista.add("CACHORRO QUENTE           "+ rs.getInt("CACHORRO")+"x R$ 7.50       (R$"+ String.format("%.2f",(rs.getInt("CACHORRO")*7.5))+")\n");
                    if(rs.getInt("MISTO")!=0)
                        lista.add("MISTO QUENTE                   "+ rs.getInt("MISTO")+"x R$ 8.00       (R$"+ String.format("%.2f",(rs.getInt("MISTO")*8.0))+")\n");
                    if(rs.getInt("FRUTAS")!=0)
                        lista.add("SALADA DE FRUTAS            "+ rs.getInt("FRUTAS")+"x R$ 5.50       (R$"+ String.format("%.2f",(rs.getInt("FRUTAS")*5.5))+")\n");
                    if(rs.getInt("REFRI")!=0)
                        lista.add("REFRIGERANTE                    "+ rs.getInt("REFRI")+"x R$ 4.50       (R$"+ String.format("%.2f",(rs.getInt("REFRI")*4.5))+")\n");
                    if(rs.getInt("SUCO")!=0)
                        lista.add("SUCO NATURAL                  "+ rs.getInt("SUCO")+"x R$ 6.25       (R$"+ String.format("%.2f",(rs.getInt("SUCO")*6.25))+")\n");
                    
                    //pra printar uma lista sem [,]
                    String formattedString = lista.toString()
                    .replace(",", "")  //remove the commas
                    .replace("[", " ")  //remove the right bracket
                    .replace("]", "");  //remove the left bracket


                    String valor_final = String.format("%.2f", rs.getDouble("VALOR_TOTAL"));             
                    
                    //mostra extrato
                    JOptionPane.showMessageDialog(null, "Extrato:\n-----------------------------------------\n"+ formattedString + "-----------------------------------------\n Valor Total: ------------------------ R$" + valor_final, "Extrato", JOptionPane.PLAIN_MESSAGE);

            }
            } catch( SQLException e) {
                System.out.println("ERRO");
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Você não possuí nenhum um pedido em andamento.", "Extrato", JOptionPane.ERROR_MESSAGE);
        } 

        
        
    }//GEN-LAST:event_extrato_bttActionPerformed

    private void cadastrar_bttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrar_bttActionPerformed
        DAO_pedido crud = new DAO_pedido();
        
        //checa se ja existe pedido com aquele cpf
        boolean rs = crud.checkPedido(cpf);
        if (rs){
            JOptionPane.showMessageDialog(null, "Você já possuí um pedido em andamento. \nPor favor, aguarde para que este seja finalizado, ou cancele-o, para fazer um novo pedido.", "Pedido em andamento", JOptionPane.ERROR_MESSAGE);
        }
        else{
            //abre interface para fazer um novo pedido
            new Novo_pedido().setVisible(true);
            this.setVisible(false);
        } 
    }//GEN-LAST:event_cadastrar_bttActionPerformed

