    private void criar_conta_bttActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_criar_conta_bttActionPerformed
        String nome = nome_txt.getText();
        String cpf = cpf_txt.getText();
        String senha = senha_txt.getText();
        
        DAO_cliente crud = new DAO_cliente();
        
        //checagem se ja existe cliente com este cpf
        
        boolean rs = crud.checkCPF(cpf);
        if(rs)
            JOptionPane.showMessageDialog(null, "JÃ¡ existe um cadastro com este CPF", "Cadastro", JOptionPane.ERROR_MESSAGE);
        else{
            //cria cliente na classe
            Cliente cliente = new Cliente(nome,cpf,senha);
            //inseri no banco de dados
            crud.inserir(cliente.getNome(),cliente.getCPF(),cliente.getSenha());
            //volta pra tela de login
            new Log_In().setVisible(true);
            this.setVisible(false);    
        }  
    }//GEN-LAST:event_criar_conta_bttActionPerformed

